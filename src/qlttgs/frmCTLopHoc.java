    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        String MaLop = txtMaLop.getText();
            String tenLop = txtTenLop.getText();
            String maMH = txtMaMH.getText();
            String maCa = txtCaHoc.getText();
            String maGV = txtMaGV.getText();
            String ngayBD = txtNgayBD.getText();
            String ngayKT = txtNgayKT.getText();
            String tongBuoi = txtTongBuoi.getText();
            String trangThai = cbxTrangThai.getSelectedItem().toString();
            pCTLop l = new pCTLop();
            l.setTenLop(tenLop);
            l.setMaCa(maCa);
            l.setMaGV(maGV);
            l.setMaMH(maMH);
            l.setNgayBD(ngayBD);
            l.setNgayKT(ngayKT);
//            l.setTongBuoi(tongBuoi);
            l.setTrangThai(trangThai);
            boolean kq = frmCTLopDAO.capNhatLop(MaLop, l);
            JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn cập nhật mã ]"+ txtMaLop.getText()+
                "]?","Xác nhận cập nhật", JOptionPane.YES_NO_OPTION);
            if ( kq == true) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công", "Thông báo",JOptionPane.INFORMATION_MESSAGE);
            ShowData();
            }
            else{
                JOptionPane.showMessageDialog(this, "Cập nhật không thành công", "Thông báo",JOptionPane.INFORMATION_MESSAGE);
            ShowData();
            }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (frmCTLopDAO.KTTT(txtMaLop.getText()) == false) {
            JOptionPane.showConfirmDialog(null, "Mã ko tồn tại!");
            return;
        }
        else{
            if (frmCTLopDAO.xoaLop(txtMaLop.getText()) == true) {
                JOptionPane.showConfirmDialog(null, "Xóa thành công!");
                ShowData();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed
   
