    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (frmCTLopDAO.KTTT(txtMaLop.getText()) == true) {
            JOptionPane.showMessageDialog(this, "Thêm lớp học thất bại","Thông báo",JOptionPane.INFORMATION_MESSAGE);
            ShowData();
        }
        else{
            String MaLop = txtMaLop.getText();
            String tenLop = txtTenLop.getText();
            String maMH = txtMaMH.getText();
            String maCa = txtCaHoc.getText();
            String maGV = txtMaGV.getText();
            String ngayBD = txtNgayBD.getText();
            String ngayKT = txtNgayKT.getText();
            String tongBuoi = txtTongBuoi.getText();
            String trangThai = cbxTrangThai.getSelectedItem().toString();
            pCTLop l = new pCTLop();
            l.setMaLop(MaLop);
            l.setTenLop(tenLop);
            l.setMaCa(maCa);
            l.setMaGV(maGV);
            l.setMaMH(maMH);
            l.setNgayBD(ngayBD);
            l.setNgayKT(ngayKT);
            l.setTongBuoi(Integer.parseInt(tongBuoi));
            l.setTrangThai(trangThai);
            boolean kq = frmCTLopDAO.themLop(l);
            if ( kq == true) {
                JOptionPane.showMessageDialog(this, "Thêm lớp học thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                ShowData();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed
    
